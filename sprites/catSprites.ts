/** Flip a sprite horizontally to generate left-facing from right-facing */
function flipH(sprite: SpriteData): SpriteData {
  return sprite.map((row) => [...row].reverse())
}

const CAT_LEFT_1 = flipH(CAT_RIGHT_1)
const CAT_LEFT_2 = flipH(CAT_RIGHT_2)

/** Build CharacterSprites for the cat. Walk uses 4 frames (stand/walk alternating).
 *  Typing and reading reuse standing pose (cats don't type). */
export function getCatSprites(): CharacterSprites {
  const walk = (s1: SpriteData, s2: SpriteData): [SpriteData, SpriteData, SpriteData, SpriteData] =>
    [s1, s2, s1, s2]
  const idle = (s: SpriteData): [SpriteData, SpriteData] => [s, s]

  return {
    walk: {
      [0]: walk(CAT_DOWN_1, CAT_DOWN_2),
      [1]: walk(CAT_LEFT_1, CAT_LEFT_2),
      [2]: walk(CAT_RIGHT_1, CAT_RIGHT_2),
      [3]: walk(CAT_UP_1, CAT_UP_2),
    },
    typing: {
      [0]: idle(CAT_DOWN_1),
      [1]: idle(CAT_LEFT_1),
      [2]: idle(CAT_RIGHT_1),
      [3]: idle(CAT_UP_1),
    },
    reading: {
      [0]: idle(CAT_DOWN_1),
      [1]: idle(CAT_LEFT_1),
      [2]: idle(CAT_RIGHT_1),
      [3]: idle(CAT_UP_1),
    },
  }
}
